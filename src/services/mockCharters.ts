// ── Query helpers ─────────────────────────────────────────────────────────────

export function getYachts(): Yacht[] {
  return MOCK_YACHTS
}

export function getYachtById(id: string): Yacht | undefined {
  return MOCK_YACHTS.find((y) => y.id === id)
}

export function searchYachts(
  params: Partial<CharterSearchParams>,
  filters: { boatType?: string; minCabins?: number; maxPrice?: number },
): Yacht[] {
  let results = [...MOCK_YACHTS]

  if (params.destination) {
    const q = params.destination.toLowerCase()
    results = results.filter(
      (y) =>
        y.country.toLowerCase().includes(q) ||
        y.region.toLowerCase().includes(q) ||
        y.city.toLowerCase().includes(q) ||
        y.marina.toLowerCase().includes(q),
    )
  }

  if (params.guests && params.guests > 1) {
    results = results.filter((y) => y.guests >= params.guests!)
  }

  if (filters.boatType && filters.boatType !== 'ALL') {
    results = results.filter((y) => y.type === filters.boatType)
  }

  if (filters.minCabins && filters.minCabins > 0) {
    results = results.filter((y) => y.cabins >= filters.minCabins!)
  }

  if (filters.maxPrice && filters.maxPrice < 20000) {
    results = results.filter((y) => y.pricePerWeekEur <= filters.maxPrice!)
  }

  return results
}
